<script setup lang="ts">
import { ref, computed } from 'vue';
// import { Checkbox } from 'vue-recaptcha';
import { RouterLink } from 'vue-router';
import Countdown from '@/components/ui/Countdown.vue';
import { getFaucetBalance, getFaucetTransaction, claimFaucetToken } from '@/lib/api';
import { getTransationUrl } from '@/lib/config';
import { useToast } from '@/components/ui/toast/use-toast'
import { Skeleton } from '@/components/ui/skeleton'
import opBNB from '@/assets/images/opBNB.svg';

const { toast } = useToast();
const address = ref('');
const sending = ref(false);
const loading = ref(true);
const transactions = ref<{ hash: string; createAt: number; }[]>([]);
const disabled = computed(() => sending.value);
// const response = ref();
const viewTransactions = computed(() => transactions.value.length ? transactions.value : [{ hash: '', createAt: 0 }]);
const addressElement = ref<HTMLInputElement | null>(null);
const restToken = ref(0);

function alertMessage(message: string) {
  toast({
    description: message
  });
}

async function doRefresh() {
  loading.value = true;
  try {
    await Promise.all([
      address.value && getFaucetTransaction({ address: address.value }).then((data) => {
        if (data.traded) {
          transactions.value = [
            {
              hash: data.hash,
              createAt: new Date(data.time).getTime(),
            }
          ]
        } else {
          transactions.value = [];
        }
      }),
      getFaucetBalance().then((data) => {
        restToken.value = data;
      })
    ])
  } finally {
    loading.value = false;
    focusAddress();
  }
}

async function onSend() {
  if (sending.value) {
    return;
  }
  if (!address.value) {
    alertMessage('Please enter your wallet address.');
    focusAddress();
    return;
  }
  // if (!response.value) {
  //   alertMessage('Please complete our reCAPTCHA and validations to make this request.');
  //   return;
  // }

  sending.value = true;

  try {
    // await claimFaucetToken({ address: address.value, respToken: response.value });
    await claimFaucetToken({ address: address.value });
    alertMessage('Success! Please wait for the transaction to be confirmed.');
  } catch (error) {
    const reason = error.message || 'An error occurred. Please try again later.';
    alertMessage(reason);
  } finally {
    sending.value = false;
    await doRefresh();
  }
}

function focusAddress() {
  setTimeout(() => {
    addressElement.value?.focus();
    addressElement.value?.click();
  }, 0);
}

doRefresh();
</script>
